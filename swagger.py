class Solution:
    def formatapilist(self, apis):
        title = ''
        api = ''
        cnt = 1
        for s in apis:
            if cnt == 1 and s != 'GET' and s != 'POST':
                cnt += 1
            elif cnt == 2 and '/' not in s:
                cnt = 1
                title = s
            elif cnt == 1 and (s == 'GET' or s == 'POST'):
                cnt += 1
                api = 'SC-'
            elif cnt == 2 and '/' in s:
                cnt += 1
                api = api + s
            elif cnt == 3:
                cnt = 1
                api = api + '-' + title + '-' + s
                print(api)


